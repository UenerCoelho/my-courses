const a = 7;
let b = 3;

// Operador de Incremento formato reduzido para b = b + a
b += a; 
console.log(b); // 10

// Operador de Incremento formato reduzido para b = b + 4
b += 4; 
console.log(b); // 14

// Operador de Multiplicação formato reduzido para b = b - 2
b *= 2;
console.log(b); // 28

// Operado de Divisão formato reduzido para b = b / 2
b /= 2;
console.log(b); // 14

// Operador Módulo (Resto da Divisão) formato reduzido para b = b % 2
b %= 2;
console.log(b); // 0