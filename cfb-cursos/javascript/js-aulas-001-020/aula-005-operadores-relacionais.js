/* Operadores Relacionais
    > Maior
    >= Maior ou Igual
    < Menor
    <= Menor ou Igual
    == Igual (dois sinais de =)
    != Diferente (1 sinal de ! e outro de =)
*/

let num1 = 1
let num2 = 2
console.log(`1 é maior que 2? ${num1 > num2}`)
console.log(`1 é menor que 2? ${num1 < num2}`)
console.log(`1 é maior ou igual que 2? ${num1 >= num2}`)
console.log(`1 é menor ou igual que 2? ${num1 <= num2}`)
console.log(`1 é igual que 2? ${num1 == num2}`)
console.log(`1 é diferente que 2? ${num1 != num2}`)
