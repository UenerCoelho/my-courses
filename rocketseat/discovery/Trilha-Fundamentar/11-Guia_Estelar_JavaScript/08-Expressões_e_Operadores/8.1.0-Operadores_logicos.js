// Operadores Lógicos (Logical Operators)

// - 2 valores booleanos, quando verificados, resultará em true or false

let pao = true
let queijo = false

//  AND &&
console.log(pao && queijo) // Neste Caso, para ser true, todos tem que ser true, mas um ou mais forem falsos, a resposta será Falsa

// OR ||
console.log(pao || queijo) // Neste Caso, qualquer um que seja verdadeiro a Resposta será Verdadeiro, só será fals se os 2 forem Falsos

// NOT !
console.log(!pao, !queijo) // Neste caso, é a negação, se negarmos o True ele se torna falso e vice e versa
