// Operadores de comparação

// Irá comparar valores e retornar um Boolean como resposta à comparação

let one = 1
let two = 2

// ==       Igual
console.log(one == 1, one == 2, one == two, two == 1, two == 2, two == one)

// !=       Diferente
console.log(one != 1, one != 2, one != two, two != 1, two != 2, two != one)

// ===      Estritamente Igual
console.log(one === 1, one === 2, one === two, two === 1, two === 2, two === one)

// !==      Estritamente Diferente
console.log(one !== 1, one !== 2, one !== two, two !== 1, two !== 2, two !== one)

// >        Maior
console.log( one > 1, one > 2, one > two, two > 1, two > 2, two > one)
// >=       Maior Igual
console.log(one >= 1, one >= 2, one >= two, two >= 1, two >= 2, two >= one)

// <        Menor
console.log(one < 1, one < 2, one < two, two < 1, two < 2, two < one)

// <=       Menor Igual
console.log(one <= 1, one <= 2, one <= two, two <= 1, two <= 2, two <= one)