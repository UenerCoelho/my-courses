var climavar = "Quente var"
climavar = "Frio" // aqui foi aplicado uma mudança sobre a var climarvar
console.log(climavar)
let climalet = "Quente let"
// climalet = "Frio 2" // aqui foi aplicado uma mudança sobre a var climalet
console.log(climalet, "Variaveis do tipo 'var' e 'let', podem mudar seus valores")

const climaconst = "Quente const"
console.log(climaconst, "Variavei do tipos const, são constantes, ou seja, não muda")