/*
    * Undefined (indefinido)

    * Null (Nulo)
        - Objeto que não possui nada dentro
        - Diferente de Indefinido
*/

console.log(null, "Um objeto nulo, que não possui nada")
console.log(undefined, "Algo sem definição, no JavaScript é algo que não existe")
console.log("Null é igual a Undefined?", null === undefined, "# Ou seja, Null não é igual (===) a undefined")