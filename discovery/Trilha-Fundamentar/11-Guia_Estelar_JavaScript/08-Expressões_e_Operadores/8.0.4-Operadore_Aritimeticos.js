/*
    Operadores Aritméticos
       
       * Multiplicação
       * Divisão
       * Soma
       * Sbtração
       
       * Resto da Divisão
       * Incremento
       * Decremento
       * Exonencial
       
*/

// Multiplicação *
console.log(3.2 * 5, 0.5 * 2, 3 * 7)

// Divisão /
console.log(10 / 2, 10 / 100, 30 / 10)

// Soma +
console.log(1 + 1, 10 + 100, 2.5 + 0.5)

// Subtração -
console.log(24 - 4, 30 - 10, 10 - 100)

// Resto (Remainder) da Divisão %
let remainder
remainder = 2 % 8
console.log(remainder, 11 % 9, 10 % 100)

// Incremento ++
let increment = 0
increment++
increment++
console.log(increment)

// Decremento
let decrement = 10
decrement--
decrement--
decrement--
console.log(decrement)

// Exponencial **
console.log(3 ** 3, 3 ** 2, 2 ** 3)